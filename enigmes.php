Ce n’était pas vraiment une base, mais surtout, une sorte de sas de protection, d’autre porte couvrait le mur de la salle, toutes avec des noms différents.
Je venais, du “Théâtre des morts”, et les autres portes portaient toutes des noms similaire, comme “Repos des Héros”, “Larme de la dame de Fer”, “Villa Savoye”, ou encore “Couché du Roi”, et bien d’autre.</br>

À peine ai-je eut posé Hiboux sur le sol, que je m'effondrai sur le sol. J’étais exténué de fatigue, de peur, de douleur.</br>
À mon réveil, seulement quelques heures avait passé, et Hiboux semblait toujours vivant, et surtout, beaucoup moins pâle.</br>
À ma grande nouvelle habitude, je décidai de décortiquer la salle dans son entièreté, chaque morceau de papier déchiré, je le ramassais, chaque inscription visible sur le mur, je la lisais, je fouillai chaque tiroir, chaque dessous-de-table.</br>
Après quelques fouilles, j’avais réunis tout ce qui me semblait intéressant, sur le sol, à côté de Hiboux, dont l’état m’inquiétait toujours.
